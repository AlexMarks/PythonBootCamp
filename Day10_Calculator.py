def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {"+":add, "-":subtract, "*":multiply, "/":divide}

num1 = int(input("What's the first number?: "))

while True:
    operator = input("What is the operation? (+, -, *, /, end): ")
    if operator == "end":
        break
    num2 = int(input("What's the second number?: "))

    if operator in operations:
        answer = operations[operator](num1, num2)
        print(f"{num1} {operator} {num2} = {answer}")
        num1 = answer
    else:
        print(f"Invalid operator. Reverting to previous answer of {num1}")


